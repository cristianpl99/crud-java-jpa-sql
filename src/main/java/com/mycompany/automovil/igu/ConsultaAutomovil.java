    private void cargarTabla() {
       //para que la tabla no sea editable 
       DefaultTableModel modeloTabla = new DefaultTableModel() {
           
           @Override
           public boolean isCellEditable(int row, int column){
               return false;
           }      
        };
       
       //titulos a la columnas
       String titulos[] = {"Id", "Modelo", "Marca", "Motor", "Color", "Patente", "Puertas"};
       modeloTabla.setColumnIdentifiers(titulos);
       
       //traer autos desde base de datos
       List<Automovil> listaAutomoviles = control.traerAutos();
       
       //setear los atos en la tabla
       if (listaAutomoviles != null){
           for (Automovil auto : listaAutomoviles){
               Object[] object = {auto.getId(), auto.getModelo(), auto.getMarca(),
               auto.getMotor(), auto.getColor(), auto.getPatente(), auto.getCantPuertas()};
               
               modeloTabla.addRow(object);
           }
       }
       
       
       
       
       tablaAutos.setModel(modeloTabla);
    }
