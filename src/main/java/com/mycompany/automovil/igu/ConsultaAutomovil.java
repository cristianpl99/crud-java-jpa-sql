    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //control de tabla vacio
        if (tablaAutos.getRowCount() > 0){
            //valido que se haya seleccionado un registro
            if(tablaAutos.getSelectedRow() != -1){
                //obtener id del auto que quiero eleminar
                    int idAuto = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                    control.borrarAuto(idAuto);
                    //muestra el mensaje de exito
                    mostrarMensaje("Auto borrado", "Info", "Borrado Exitoso");
                 //refresca la tabla
                    cargarTabla();
            }
            else{
                 mostrarMensaje("No selecciono un registro para eliminar", "Error", "Borrado con Errores");
            }
        }
        else{
             mostrarMensaje("La tabla esta vacia", "Error", "Borrado con Errores");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
          //control de tabla vacio
         if (tablaAutos.getRowCount() > 0){
            //valido que se haya seleccionado un registro
                  if(tablaAutos.getSelectedRow() != -1){
                       int idAuto = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                        modifAuto modif  = new modifAuto(idAuto);
                        modif.setVisible(true);
                        modif.setLocationRelativeTo(null);
                        
                        this.dispose();
            }
                  else{
                 mostrarMensaje("No selecciono un registro para modificar", "Error", "Modificacion con Errores");
            }
        }
        else{
             mostrarMensaje("La tabla esta vacia", "Error", "Modificacion con Errores");
        }  
    }//GEN-LAST:event_btnModificarActionPerformed
    
    public void mostrarMensaje (String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info)")){
           optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    
